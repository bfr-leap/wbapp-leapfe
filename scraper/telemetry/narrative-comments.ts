function getOrdinalSuffix(position: number): string {
    const tens = position % 100;
    const units = position % 10;
    if (tens >= 11 && tens <= 13) {
        return 'th';
    }
    switch (units) {
        case 1:
            return 'st';
        case 2:
            return 'nd';
        case 3:
            return 'rd';
        default:
            return 'th';
    }
}

